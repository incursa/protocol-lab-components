func bytesOf(value string) []byte {
	b, err := hex.DecodeString(value)
	if err != nil {
		panic(err)
	}
	return b
}
func hashOf(value []byte) string { sum := sha256.Sum256(value); return hex.EncodeToString(sum[:]) }
func getValue(f fixture) string  { return base64.RawURLEncoding.EncodeToString(bytesOf(f.QueryHex)) }
func validateFixture(f fixture) error {
	query, response := bytesOf(f.QueryHex), bytesOf(f.ResponseHex)
	if hashOf(query) != f.QueryHash || hashOf(response) != f.ResponseHash {
		return fmt.Errorf("%s canonical hash mismatch", f.ScenarioID)
	}
	var q, r dns.Msg
	if err := q.Unpack(query); err != nil {
		return err
	}
	if err := r.Unpack(response); err != nil {
		return err
	}
	if q.Id != 0 || r.Id != 0 || len(q.Question) != 1 || q.Question[0].Name != f.QuestionName || dns.TypeToString[q.Question[0].Qtype] != f.QuestionType || dns.RcodeToString[r.Rcode] != f.RCode {
		return fmt.Errorf("%s semantic mismatch", f.ScenarioID)
	}
	return nil
}
func semanticEqual(actual []byte, f fixture) bool {
	var a, e dns.Msg
	return a.Unpack(actual) == nil && e.Unpack(bytesOf(f.ResponseHex)) == nil && reflect.DeepEqual(&a, &e)
}
